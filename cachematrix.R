## These functions are to cache the inverse of a matrix

## makeCacheMatrix will create a matrix and have the ability to cache its inverse


  makeCacheMatrix <- function(x = matrix()) {
    i <- NULL
    set <- function(y) {
      x <<- y
      i <<- NULL
    }
    get <- function() x
    setinverse <- function(inverse) i <<- inverse
    getinverse <- function() i
    list(set = set,
         get = get,
         setinverse = setinverse,
         getinverse = getinverse)
  }
}


## calculate the inverse of makeCacheMatrix. This new function
## should retrieve the inverse from the cache if inverse has 
##already been calculated above

cacheSolve <- function(x, ...) {
  i <- x$getinverse()
  if (!is.null(i)) {
    message("getting cached data")
    return(i)
  }
  data <- x$get()
  i <- solve(data, ...)
  x$setinverse(i)
  i
}